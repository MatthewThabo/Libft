#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	d;
	size_t	s;

	d = 0;
	s = 0;
	while (s1[d] != '\0')
		d++;
	while (s2[s] != '\0' && s < n)
	{
		s1[d + s] = s2[s];
		s++;
	}
	s1[d + s] = '\0';
	return (s1);
}
